/* Load this script using conditional IE comments if you need to support IE 7 and IE 6. */

window.onload = function() {
	function addIcon(el, entity) {
		var html = el.innerHTML;
		el.innerHTML = '<span style="font-family: \'icomoon-ultimate\'">' + entity + '</span>' + html;
	}
	var icons = {
			'icon-github' : '&#xe000;',
			'icon-facebook' : '&#xe001;',
			'icon-comments' : '&#xe002;',
			'icon-reading' : '&#xe003;',
			'icon-puzzle' : '&#xe004;',
			'icon-color-palette' : '&#xe005;',
			'icon-mail' : '&#xe013;',
			'icon-arrow-down' : '&#xe006;',
			'icon-plus' : '&#xe007;',
			'icon-minus' : '&#xe008;',
			'icon-arrow-left' : '&#xe009;'
		},
		els = document.getElementsByTagName('*'),
		i, attr, html, c, el;
	for (i = 0; ; i += 1) {
		el = els[i];
		if(!el) {
			break;
		}
		attr = el.getAttribute('data-icon');
		if (attr) {
			addIcon(el, attr);
		}
		c = el.className;
		c = c.match(/icon-[^\s'"]+/);
		if (c && icons[c[0]]) {
			addIcon(el, icons[c[0]]);
		}
	}
};